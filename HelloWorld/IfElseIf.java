public class IfElseIf {
    public static void main(String[] args) {
        int x = 1;
        if (x < 0){
            System.out.println("It's a Negative Number");
        } else if (x > 0) {
            System.out.println("It's a Positive Number");
        } else {
            System.out.println("Its a Zero");
        }
    }
}